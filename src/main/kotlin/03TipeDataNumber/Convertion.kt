package `03TipeDataNumber`

fun main() {
    val angka1 = 100
    val byte: Byte = angka1.toByte()
    val short: Short = angka1.toShort()
    val int: Int = angka1.toInt()
    val long: Long = angka1.toLong()
    val float: Float = angka1.toFloat()
    val double: Double = angka1.toDouble()

    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)
}