package `10OperasiMatematika`

fun main() {
    var total = 0

    total++ // total = total + 1
    total++
    total++

    println(total)

    total-- // total = total -1
    total--

    println(total)
    println()

    val sehat = true
    println(!sehat) //operasi unari bolean not
    println()

    val suhu = -4
    println("suhu $suhu") // operasi unary negative
}